package operadores_cumulativos;

// Módulo 04 / Capítulo 03 / aula 06 - Operadores de atribuição cumulativa em Java

public class Main
{
   public static void main(String[] args)
   {
      System.out.println("\nOPERADORES CUMULATIVOS:");
      System.out.println("-----------------------");
      System.out.println("Símbolo | Operador ");
      System.out.println(" a += b | a = a + b");
      System.out.println(" a -= b | a = a - b");
      System.out.println(" a *= b | a = a * b");
      System.out.println(" a /= b | a = a / b");
      System.out.println(" a %= b | a = a % b");
      System.out.println("-----------------------");
   }
}
